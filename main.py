# Newline (\n)
print("Hello\nWorld")

# Tab (\t)
print("This is a\tTab")

# Backslash (\\)
print("C:\\Users\\John")

# Single Quote (\')
print("I\'m learning Python")

# Double Quote (\")
print("She said, \"Hello!\"")

# Backspace (\b)
print("Back\bSpace")

# Carriage Return (\r)
print("Carriage\rReturn")

# Formfeed (\f)
print("Page1\fPage2")

# Octal value (\ooo)
print("\110\145\154\154\157")  # Output: Hello (ASCII values for 'H', 'e', 'l', 'l', 'o')

# Hex value (\xhh)
print("\x48\x65\x6c\x6c\x6f")  # Output: Hello (Hexadecimal values for 'H', 'e', 'l', 'l', 'o')


